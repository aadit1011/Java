class Swapping
{
     public static void main(String [] args)
     {
          int a=6;
          int b=1;
          System.out.println("a="+a);
          System.out.println("b="+b);
          int temp;
          temp=a;
          a=b;
          b=temp;
          System.out.println("a="+a);
          System.out.println("b="+b);
     }
}